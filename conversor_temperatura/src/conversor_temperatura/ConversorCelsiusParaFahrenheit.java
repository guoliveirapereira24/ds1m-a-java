package conversor_temperatura;

public class ConversorCelsiusParaFahrenheit {

	
	public static void main (String[] args) {
		
		// Variable
		
		float C;
		
		float F;
		
		// Value of variable
		
			// C=Celsius
		C= 49f;
		
		// Use of variables	
		
			// F=Fahrenheit
		F= (9*C+160) /5;
				
		System.out.println(C + " graus celsius equivale a " +  F + " graus Fahrenheit: ");		
		
		
		
		
	}
	
	
	
	
}
